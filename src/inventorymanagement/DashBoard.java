    public void Show_Totals(){
       
        con=DataBaseHelperClass.ConnecrDB();
        String sql1="SELECT sum(Price) as total from Sales";
        String sql2="SELECT sum(Price) as total from Buys";
        try {
            Statement st1 = con.createStatement();
            Statement st2 = con.createStatement();
            ResultSet rs1 = st1.executeQuery(sql1);
            ResultSet rs2 = st2.executeQuery(sql2);
            float sales= Float.parseFloat(rs1.getString("total"));
            float buys=Float.parseFloat(rs2.getString("total"));
            float total = (sales-buys);
            System.out.print(total);
            TotalSales.setText(rs1.getString("total")+"TK");
            Totalbuys.setText(rs2.getString("total")+"TK");
            TotalProfit.setText(Float.toString(total)+"TK");
        }
       catch(Exception e){
            JOptionPane.showMessageDialog(null,"No data");
        }
    }
        
    

    
